@dataclass
class RegimeConfig:
    adx_threshold: float = 25.0
    atr_percentiles: List[float] = field(default_factory=lambda: [20.0, 80.0])


@dataclass
class MonitoringConfig:
    data_latency_threshold: int = 500
    api_errors_threshold: int = 5
    cpu_threshold: float = 80.0
    memory_threshold: int = 2048


@dataclass
class SystemConfig:
    version: str = "4.4.1"
    created_at: datetime = field(default_factory=_utcnow)
    hash: str = ""
    pairs: List[str] = field(default_factory=lambda: ["BTCUSDT", "ETHUSDT", "SOLUSDT", "BNBUSDT"])
    timeframes: List[str] = field(default_factory=lambda: ["15m", "1h", "4h", "1d"])
    lookback_days: int = 90
    service_mode: ServiceMode = ServiceMode.ONLINE
    strategy: Dict[StrategyType, StrategyConfig] = field(default_factory=dict)
    risk: RiskConfig = field(default_factory=RiskConfig)
    regime: RegimeConfig = field(default_factory=RegimeConfig)
    execution: ExecutionConfig = field(default_factory=ExecutionConfig)
    learning: LearningConfig = field(default_factory=LearningConfig)
    monitoring: MonitoringConfig = field(default_factory=MonitoringConfig)
