@dataclass
class OHLCV:
    """OHLCV candle data."""
    timestamp: datetime = field(default_factory=_utcnow)
    pair: str = ""
    timeframe: str = "1h"
    open: float = 0.0
    high: float = 0.0
    low: float = 0.0
    close: float = 0.0
    volume: float = 0.0

    def to_dict(self) -> dict:
        return {
            "timestamp": self.timestamp,
            "pair": self.pair,
            "timeframe": self.timeframe,
            "open": self.open,
            "high": self.high,
            "low": self.low,
            "close": self.close,
            "volume": self.volume,
        }


@dataclass
class Signal:
    """Trading signal generated by Strategy Engine."""
    pair: str = ""
    direction: Direction = Direction.LONG
    entry_market: float = 0.0
    entry_limit: float = 0.0
    stop_loss: float = 0.0
    tp1: float = 0.0
    tp2: float = 0.0
    strategy: StrategyType = StrategyType.SWEEP
    confidence: float = 0.0
    regime: str = ""
    factors: List[Dict] = field(default_factory=list)
    timestamp: datetime = field(default_factory=_utcnow)

    def __post_init__(self):
        if not 0.0 <= self.confidence <= 1.0:
            raise ValueError(f"Confidence must be between 0 and 1, got {self.confidence}")


@dataclass
class Trade:
    """Trade record."""
    trade_id: UUID = field(default_factory=uuid4)
    timestamp: datetime = field(default_factory=_utcnow)
    pair: str = ""
    direction: Direction = Direction.LONG
    entry_price: float = 0.0
    exit_price: Optional[float] = None
    size: float = 0.0
    pnl: Optional[float] = None
    fees: float = 0.0
    strategy: StrategyType = StrategyType.SWEEP
    confidence: float = 0.0
    regime: Regime = Regime.TREND_HIGH_VOL
    signal: Optional[Signal] = None

    @property
    def is_closed(self) -> bool:
        return self.exit_price is not None


@dataclass
class Position:
    """Current open position."""
    pair: str = ""
    direction: Direction = Direction.LONG
    entry_price: float = 0.0
    size: float = 0.0
    stop_loss: float = 0.0
    tp1: float = 0.0
    tp2: float = 0.0
    strategy: str = ""
    opened_at: datetime = field(default_factory=_utcnow)
    current_pnl: float = 0.0
    trade_id: UUID = field(default_factory=uuid4)
    stop_order_id: Optional[str] = None
    tp1_order_id: Optional[str] = None
    tp2_order_id: Optional[str] = None


@dataclass
class ExecutionRecord:
    """Quality-of-execution log entry."""
    timestamp: datetime = field(default_factory=_utcnow)
    pair: str = ""
    expected_price: float = 0.0
    actual_price: float = 0.0
    slippage: float = 0.0
    latency: float = 0.0  # milliseconds
    partial_fill: bool = False
    cancelled: bool = False

    @property
    def slippage_pct(self) -> float:
        if self.expected_price == 0:
            return 0.0
        return abs(self.slippage / self.expected_price)


